package com.dimas.uasandroid;

public class Model {
    int id;
    String title;
    String body;

    public Model(int id, String title, String body) {
        this.id = id;
        this.title = title;
        this.body = body;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }
}
